    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTNome.getText().equals("") || jTSobrenome.getText().equals("") || txtCPF.getText().equals("") || txtCel.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor preencha corretamente os campos!!!");
        } else {
            JOptionPane.showMessageDialog(null, "Cadastro Atualizado!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTNomeKeyReleased(java.awt.event.KeyEvent evt) {                                     
        // TODO add your handling code here:
        for (int i = 0; i < jTNome.getText().length(); i++) {
            char letra = jTNome.getText().charAt(i);
            if (Character.isDigit(letra)) {
                JOptionPane.showMessageDialog(null, "Digite teu nome de forma correta!");
                jTNome.setText("");
            }
        }
    }                                    

    private void jTSobrenomeKeyReleased(java.awt.event.KeyEvent evt) {                                     
        // TODO add your handling code here:
        for (int i = 0; i < jTSobrenome.getText().length(); i++) {
            char letra = jTSobrenome.getText().charAt(i);
            if (Character.isDigit(letra)) {
                JOptionPane.showMessageDialog(null, "Digite teu nome de forma correta!");
                jTSobrenome.setText("");
            }
        }
    }  
    
